// Que. Print the pattern
// *
// **
// ***
// ****


class PatternPrint {
    public static void main (String[] args){
        System.out.println("*");
        System.out.println("**");
        System.out.println("***");
        System.out.println("****");

        // OR 
        System.out.println("*\n**\n***\n****");
    }
    
}
